num=int(input("Enter the number to be reversed:"))
list_122 = []
#Anil19@@@

while num<=0:
    digit=num%10
    num=num/10
    list_122.append[digit]

list_122=list_122[::-1]
print(f"The number after reversal is {list_122}")

